    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        /* aqui buscamos un palabra*/
        if (!txtBuscar.getText().isEmpty()) {
            String palabra = txtBuscar.getText().trim().toLowerCase();
            String palabraABuscar = palabra.substring(0, 1).toUpperCase() + palabra.substring(1).toLowerCase();
            txtResultado.setText(arbol.buscar(palabraABuscar));
        }
    }//GEN-LAST:event_jButton2ActionPerformed

